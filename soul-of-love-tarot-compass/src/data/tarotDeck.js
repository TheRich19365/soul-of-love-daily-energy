const correspondenceById = Object.fromEntries(
  majorCorrespondences.map(([id, correspondence, derivedElement, direction]) => [id, { correspondence, derivedElement, direction }]),
);

function slugify(name) {
  return name.toLowerCase().replaceAll(" ", "-");
}

function majorCard(index, [nameEn, nameTh, core, light, action, shadow, question]) {
  const id = slugify(nameEn);
  const map = correspondenceById[id];
  const element = map.derivedElement;
  return {
    id,
    number: index,
    rank: null,
    nameEn,
    nameTh,
    arcana: "Major",
    suit: null,
    element,
    direction: map.direction,
    correspondenceType: "Major",
    correspondence: map.correspondence,
    derivedElement: element,
    keywordsUpright: [core, light, action],
    keywordsReversed: [shadow, "ทบทวน", "ปรับสมดุล"],
    meaningUpright: `${nameTh} สะท้อน ${core} และชวนให้ใช้ ${light} ผ่าน ${action} อย่างมีสติ`,
    meaningReversed: `${nameTh} กลับหัวไม่ได้แปลว่าร้าย แต่ชวนมอง ${shadow} และค่อย ๆ ปรับพลังให้กลับมาสมดุล`,
    reflectionQuestion: question,
    imagePath: "",
    visualSymbol: elementInfo[element].symbol,
  };
}

