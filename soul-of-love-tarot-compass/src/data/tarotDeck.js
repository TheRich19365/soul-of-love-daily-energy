function minorCard([rankEn, rankTh, rankCore, rankLight, rankAction, rankShadow], suit) {
  const lens = suitLens[suit];
  const suitMap = suitCorrespondences[suit];
  const element = lens.element;
  const direction = suitMap.direction;
  const nameEn = `${rankEn} of ${suit}`;
  const nameTh = `${rankTh} ${lens.th}`;
  return {
    id: `${rankEn.toLowerCase()}-of-${suit.toLowerCase()}`,
    number: null,
    rank: rankEn,
    nameEn,
    nameTh,
    arcana: "Minor",
    suit,
    element,
    direction,
    correspondenceType: "Suit",
    correspondence: `${suit} / ${element}`,
    derivedElement: element,
    keywordsUpright: [rankCore, rankLight, ...lens.themes.slice(0, 1)],
    keywordsReversed: [rankShadow, "ทบทวน", lens.themes[1]],
    meaningUpright: `${nameTh} สะท้อน ${rankCore} ในสนามของ${lens.themes[0]} ${lens.uprightBase} และเหมาะกับการ${rankAction}`,
    meaningReversed: `${nameTh} กลับหัวชวนมอง ${rankShadow} ${lens.reversedBase} โดยไม่ตัดสินตัวเอง`,
    reflectionQuestion: `วันนี้คุณจะใช้${lens.themes[0]}กับ${rankCore}ให้สมดุลขึ้นได้อย่างไร`,
    imagePath: "",
    visualSymbol: suitMap.symbol,
  };
}

