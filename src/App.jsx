function randomItem(items) {
  return items[Math.floor(Math.random() * items.length)];
}

function randomBetween(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function createReading() {
  const card = randomItem(tarotDeck);
  const orientation = Math.random() > 0.5 ? "upright" : "reversed";
  const aura = auraPalette[randomItem(card.auraColors)];
  const [minEnergy, maxEnergy] = card.energyRange;
  const score = randomBetween(minEnergy, maxEnergy);

  return {
    id: `${card.id}-${Date.now()}-${Math.random().toString(36).slice(2)}`,
    card,
    orientation,
    aura,
    energyScore: score,
    soulMessage: randomItem(card.soulMessages),
    warning: randomItem(card.warnings),
    focusPoint: randomItem(card.focusPoints),
    emotionalState: randomItem(card.emotionalStates),
    guidance: `${orientation === "upright" ? card.uprightMeaningThai : card.reversedMeaningThai} ${card.guidanceText}`,
    love: makeAreaInsight("love", card, orientation),
    work: makeAreaInsight("work", card, orientation),
    money: makeAreaInsight("money", card, orientation)
  };
}

