export const areaTemplates = Object.fromEntries(
  Object.entries(areaPatternBanks).map(([area, variants]) => [
    area,
    (context) => variants[stableVariantIndex(area, context, variants.length)]?.(context) || sectionFallback(context, area)
  ])
);

export function buildAreaText(area, context) {
  const variants = areaPatternBanks[area];
  if (!variants?.length) {
    return diversifyAreaText(area, cleanThaiKeywords(sectionFallback(context, area), context.language), context);
  }

  const selected = variants[stableVariantIndex(area, context, variants.length)];
  const text = selected?.(context) || sectionFallback(context, area);
  return diversifyAreaText(area, cleanThaiKeywords(text, context.language), context);
}
