export function composeKnowledgeAreaText(area, context) {
  const patterns = areaPatterns[area];
  if (!patterns?.length) return buildAreaText(area, context);
  const text = pick(patterns, context, area, "pattern")(context);
  if (["universe", "mission", "affirmation"].includes(area)) {
    return text.replace(/\s+/g, " ").trim();
  }
  return diversifyAreaText(area, text, context);
}
