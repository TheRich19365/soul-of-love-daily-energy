function auraLens(context, area) {
  const bank = auraLensBank[auraKey(context)];
  if (bank?.[area]) return bank[area];

  const fallback = {
    universe: context.auraProfile.coreMeaning,
    love: context.auraProfile.relationshipLens,
    work: context.auraProfile.workLens,
    money: context.auraProfile.moneyLens,
    focus: context.auraProfile.focusLens,
    warning: context.auraProfile.warningLens,
    mission: context.auraProfile.missionTone
  };

  return fallback[area] || context.auraProfile.coreMeaning;
}

