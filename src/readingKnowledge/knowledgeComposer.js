function stableIndex(key, count) {
  const value = Array.from(String(key)).reduce((sum, char, index) => sum + char.charCodeAt(0) * (index + 11), 0);
  return count ? value % count : 0;
}

function pick(list, context, area, salt) {
  return list[stableIndex(`${context.cardAspect.id}|${context.aura?.english}|${context.aspect}|${context.intensity.id}|${area}|${salt}`, list.length)];
}

function aspectExpression(context) {
  return context.aspect === "Light" ? context.cardAspect.lightExpression : context.cardAspect.shadowExpression;
}

function aspectCue(context) {
  if (context.aspect === "Light") {
    return `พลังเปิดกำลังให้ใช้ ${context.cardAspect.gift}`;
  }

  return `พลังสะท้อนกำลังชี้ให้เห็น ${context.cardAspect.blindSpot}`;
}

function auraKey(context) {
  return context.aura?.english || context.auraProfile?.enName || context.auraProfile?.id || "Default";
}

