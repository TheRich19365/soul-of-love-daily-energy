import { randomItem } from './random.js';

export function makeAreaInsight(area, card, orientation) {
  const shadowNote = orientation === "reversed" ? "แต่วันนี้ควรช้าลงและฟังเงาในใจก่อนตอบสนอง" : "และวันนี้พลังนี้เปิดทางให้คุณขยับอย่างซื่อสัตย์";
  const templates = {
    love: [
      `ความรักวันนี้สะท้อนธีม "${card.energyTheme}" ให้เลือกความสัมพันธ์ที่ทำให้คุณกลับมาใกล้ตัวเอง ${shadowNote}`,
      `หัวใจต้องการความจริงที่อ่อนโยน ไพ่ ${card.thaiName} ชวนให้พูดความรู้สึกแบบไม่ทำร้ายทั้งตัวเองและอีกฝ่าย`,
      `พลังรักวันนี้ไม่ได้เร่งให้ตัดสินใคร แต่ชวนให้สังเกตว่าใครหรืออะไรทำให้หัวใจของคุณขยายตัว`
    ],
    work: [
      `งานและภารกิจวันนี้ต้องการจังหวะที่ชัด ไพ่ ${card.thaiName} ชวนให้เลือกหนึ่งเรื่องสำคัญแล้วทำด้วยพลังที่นิ่ง`,
      `อย่ากระจายพลังไปทุกทาง ให้ธีม "${card.energyTheme}" เป็นเข็มทิศในการจัดลำดับงาน`,
      `ภารกิจวันนี้จะเบาขึ้นเมื่อคุณแยกสิ่งจำเป็นออกจากเสียงกดดันรอบตัว`
    ],
    money: [
      `การเงินและความมั่นคงวันนี้ขอให้ใช้ทรัพยากรด้วยสติ ความมั่นคงเริ่มจากการรู้ว่าพลังรั่วตรงไหน`,
      `ไพ่ ${card.thaiName} ชวนให้มองเงินเป็นพลังงานของการดูแล ไม่ใช่เครื่องมือปลอบความกังวลชั่วคราว`,
      `วันนี้เหมาะกับการตัดสินใจทางการเงินแบบชัด เรียบ และไม่เร่งตามอารมณ์`
    ]
  };

  return randomItem(templates[area]);
}
